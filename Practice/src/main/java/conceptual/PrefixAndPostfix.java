public class PrefixAndPostfix {

    public static void main(String[] args){
        for(int i = 0; i < 2; i ++){
            System.out.println("Postfix");
        }

        for(int i = 0; i < 2; ++i){
            System.out.println("Prefix");
        }

    }
}
