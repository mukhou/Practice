package com.starandnumbertriangles;

public class RightAngleNumberTree {

	public static void main(String args[]) {

		
		int i, j, n = 5;
		System.out.println("Please enter a number  ");
		//n = Integer.parseInt(args[0]);

		for (i = 1; i <= n; i++) {
			for (j = 1; j <= i; j++) {
				System.out.print(j);				
			}
			
			System.out.println("");
			
			
		
		}
	}
}
	


